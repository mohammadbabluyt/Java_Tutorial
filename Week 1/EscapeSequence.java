/*

\n
\t
\"
\'

*/

public class EscapeSequence {
	public static void main(String[] args) {
		
		System.out.println("Mohammad Bablu YT \n2024"); // \n diye new line create kore.
		
		System.out.println("A B C \t D"); // \t diye tab create kore.
		
		System.out.println(" \"Bablu\" "); // \" double Quote create kore.
		
		System.out.println("Good Morning \'Bablu\'"); // \' single quote create kore.
		
	}
}