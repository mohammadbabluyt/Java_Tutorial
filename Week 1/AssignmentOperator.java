public class AssignmentOperator {
	public static void main(String[] args) {
		
		int x = 7;
		int y = 3;
		
		x+=y; // x = x + y = 10
		System.out.println("x = "+x);
		
		x-=y; // x = x - y = 7
		System.out.println("x = "+x);
		
		x*=y; // x = x * y = 21
		System.out.println("x = "+x);
		
		x/=y; // x = x / y = 7
		System.out.println("x = "+x);
		
		x%=y;  // x = x % y = 1
		System.out.println("x = "+x);
		
	}
}